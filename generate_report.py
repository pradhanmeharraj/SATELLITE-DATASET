"""
Generate a HTML report from evaluation results using existing visualization utilities.
"""
import json
from pathlib import Path
import sys
import numpy as np

# Ensure src is importable
sys.path.insert(0, str(Path(__file__).parent / "src"))
from visualization import plot_trajectory, plot_error_distribution
from evaluation import translation_error, quaternion_distance

ROOT = Path(__file__).parent
RESULTS = ROOT / "results"
EVAL_FILE = RESULTS / "evaluation_results.json"

if not EVAL_FILE.exists():
    raise SystemExit(f"Evaluation results not found: {EVAL_FILE}")

with open(EVAL_FILE, "r") as f:
    data = json.load(f)

metrics = data.get("metrics", {})
preds = data.get("predictions", [])
gts = data.get("ground_truth", [])

# Save trajectory plot
trajectory_path = RESULTS / "trajectory.png"
try:
    plot_trajectory(gts, preds, output_path=trajectory_path, title="Trajectory Comparison (GT vs Pred)")
except Exception as e:
    print("Failed to create trajectory plot:", e)

# Compute errors
trans_errors = []
rot_errors = []
for p, g in zip(preds, gts):
    p_t = np.array(p["translation"]) if isinstance(p["translation"], list) else np.array(p["translation"])
    g_t = np.array(g["translation"]) if isinstance(g["translation"], list) else np.array(g["translation"])
    p_q = np.array(p["quaternion"]) if isinstance(p["quaternion"], list) else np.array(p["quaternion"])
    g_q = np.array(g["quaternion"]) if isinstance(g["quaternion"], list) else np.array(g["quaternion"])

    trans_errors.append(float(translation_error(p_t, g_t)))
    rot_errors.append(float(quaternion_distance(p_q, g_q)))

# Save error distribution plot
error_path = RESULTS / "error_distribution.png"
try:
    plot_error_distribution(trans_errors, rot_errors, output_path=error_path)
except Exception as e:
    print("Failed to create error distribution plot:", e)

# Create HTML report
report_path = RESULTS / "hybrid_report.html"
html = f"""
<!doctype html>
<html>
<head>
  <meta charset="utf-8">
  <title>Hybrid Pose Estimation Report</title>
  <style>
    body {{ font-family: Arial, sans-serif; margin: 20px; }}
    h1,h2,h3 {{ color: #222; }}
    .card {{ border:1px solid #ddd; padding: 12px; margin: 8px 0; background:#fafafa; }}
    table {{ border-collapse: collapse; width: 100%; margin-top: 12px; }}
    th,td {{ border:1px solid #ccc; padding:8px; text-align:left; }}
    th {{ background:#2f6f9f; color:white; }}
    img {{ max-width:100%; height:auto; }}
  </style>
</head>
<body>
  <h1>Hybrid Pose Estimation — Results</h1>
  <p>Summary metrics and visualizations generated from evaluation results.</p>

  <h2>Summary Metrics</h2>
  <div class="card">
    <table>
      <tr><th>Metric</th><th>Value</th></tr>
"""
for k, v in metrics.items():
    html += f"      <tr><td>{k}</td><td>{v}</td></tr>\n"
html += f"    </table>\n  </div>\n"

html += "\n  <h2>Simulation Visuals</h2>\n"
html += "  <h3>Trajectory Comparison</h3>\n"
html += f"  <img src=\"{trajectory_path.name}\" alt=\"Trajectory\">\n"
html += "  <h3>Error Distribution</h3>\n"
html += f"  <img src=\"{error_path.name}\" alt=\"Error Distribution\">\n"

html += "\n  <h2>Per-sample Predictions (first 10)</h2>\n"
html += "  <table>\n    <tr><th>#</th><th>Pred Trans (x,y,z)</th><th>GT Trans (x,y,z)</th><th>Trans Error (m)</th><th>Rot Error (deg)</th></tr>\n"
for i, (p, g) in enumerate(zip(preds, gts)):
    if i >= 10:
        break
    p_t = p['translation']
    g_t = g['translation']
    te = trans_errors[i]
    re = rot_errors[i]
    html += f"    <tr><td>{i+1}</td><td>{tuple(np.round(p_t,3))}</td><td>{tuple(np.round(g_t,3))}</td><td>{te:.3f}</td><td>{re:.3f}</td></tr>\n"
html += "  </table>\n"

html += "\n  <footer><p>Generated by project scripts.</p></footer>\n</body>\n</html>"

with open(report_path, 'w', encoding='utf-8') as f:
    f.write(html)

print(f"Report generated: {report_path}")
print("Make sure to open the file from the results directory to view images.")
